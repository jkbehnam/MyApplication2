package com.example.behnam.myapplication.objects;

public class Animation {
    private String anim_type;
    private String anim_name;
    private String anim_text;
    private String anim_video;

    public String getAnim_type() {
        return anim_type;
    }

    public void setAnim_type(String anim_type) {
        this.anim_type = anim_type;
    }

    public String getAnim_name() {
        return anim_name;
    }

    public void setAnim_name(String anim_name) {
        this.anim_name = anim_name;
    }

    public String getAnim_text() {
        return anim_text;
    }

    public void setAnim_text(String anim_text) {
        this.anim_text = anim_text;
    }

    public String getAnim_video() {
        return anim_video;
    }

    public void setAnim_video(String anim_video) {
        this.anim_video = anim_video;
    }
}
